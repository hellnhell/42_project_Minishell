#include "minishell.h"

int     ft_env(char **env)
{
	int     l;

	l = -1;
	while (env[++l])
	{
		ft_putstr_fd(env[l], 1);
		if (env[l + 1])
			ft_putchar_fd('\n', 1);
	}
	return (0);
}
